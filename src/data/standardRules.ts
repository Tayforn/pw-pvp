// =========================================================
// Стандартні правила турнірів за розміром команди (teamSize) — той самий
// показник, що вже є в tournaments.team_size (null/1 = 1х1, 2 = 2х2, тощо).
// Використовується і публічною сторінкою "Правила", і кнопкою швидкої
// вставки в редакторі турніру.
// =========================================================

export interface RuleSection {
  title: string;
  points: string[];
}

export const RULE_SECTIONS: RuleSection[] = [
  {
    title: '1х1',
    points: [
      'Заборонено використовувати 3 ци, будь-які види імунітету та аптечку (дозволені лише таблетки на стихійну шкоду).',
      'Кайт не може тривати більше 15 секунд (кайтом вважається біг або відсутність атак по супернику понад 15 секунд).',
      'Для танків заборонена черепаха проти фізичних класів (сін вар танк), а облог можна використовувати не довше 15 секунд.',
      'Воїнам заборонено перебафувати «Ауру сталі» проти фізичних класів (сін вар танк, дозволено один раз на 20 секунд).',
      'Сінам заборонено використовувати «Лінь» проти фізичних класів (сін вар танк).',
      'Друїдам заборонена комбінація «0 + Парадокс».',
    ],
  },
  {
    title: '2х2',
    points: [
      'Діють ті самі обмеження, що й у 1х1: заборонені 3 ци, будь-які види імунітету, аптечка (крім таблеток на стихійну шкоду) та кайт понад 15 секунд.',
      'Заборонені однакові класи в команді.',
      "Заборонена зв'язка Містик + Прист.",
      'Заборонена комбінація навичок «0 + Парадокс».',
    ],
  },
  {
    title: '3х3, 5х5, 6х6, 10х10',
    points: ['Без правил.'],
  },
];

/** Правила для конкретного розміру команди турніру (null/1 = одноосібний
 * 1х1) — текст для вставки у вільне поле "Правила" турніру. */
export function standardRulesFor(teamSize: number | null): string | null {
  const section = !teamSize || teamSize === 1 ? RULE_SECTIONS[0] : teamSize === 2 ? RULE_SECTIONS[1] : [3, 5, 6, 10].includes(teamSize) ? RULE_SECTIONS[2] : null;
  return section ? section.points.map((p) => `• ${p}`).join('\n') : null;
}

export function standardRulesLabel(teamSize: number | null): string {
  if (!teamSize || teamSize === 1) return '1х1';
  if (teamSize === 2) return '2х2';
  return `${teamSize}х${teamSize}`;
}
