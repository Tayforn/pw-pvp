-- Частіша перевірка автостворення турнірів: раз на добу о 03:00 було
-- незручно (після переносу/видалення турніру наступний з'являвся аж
-- наступного ранку) — тепер кожні 2 години. Функція ідемпотентна
-- (перевіряє існування турніру на дату), тож частота безпечна.
-- cron.schedule з тим самим іменем оновлює наявний джоб, а не додає другий.
select cron.schedule('create-due-series-tournaments', '0 */2 * * *', 'select create_due_series_tournaments();');
